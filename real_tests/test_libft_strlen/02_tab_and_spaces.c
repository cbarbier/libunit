#include "../libft_test.h"

int	tab_and_spaces(void)
{
	int		i;
	int		j;

	i = ft_strlen("		1 2 	\t");
	j = strlen("		1 2 	\t");
	if (i == j)
		return (0);
	else
		return (-1);
}
